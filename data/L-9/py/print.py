# 用end = '' 連接下行print()
print('Hello', end = '')
print(' World', end = '')
print('!')

print('') # 空一行

# , 但表控一格
print('cat', 'dog', 'mice')

print('')

# 不將上 + ,也可以連結'文字'跟'文字'
print("print('cat' 'dog' 'mice') = print('cat' + 'dog' + 'mice') 顯示如下")
print('=>', 'cat' + 'dog' + 'mice')

print('')

# 用sep在'文字'跟'文字'之間加入文字
print('cat', 'dog', 'mice', sep=", ")
print('cat', 'dog', 'mice', sep="123")

