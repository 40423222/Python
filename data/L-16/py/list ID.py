### 列表共用性
print("列表共用性")
print('變數值可以互換:')
print('定義: 百 = 10 => 十 = 百 => 百 = 100')
百 = 10
十 = 百
百 = 100
print("十:", 十)
print("百:", 百)
print("說明: 改變百的值 十的值不會跟著變化")
print('')

print("列表是不可互換:")
print("定義: 數列1 = [1, 1, 2]")
數列1 = [1, 1, 2]
print("執行: 數列2 = 數列1")
數列2 = 數列1
print("執行: 數列2[2] = 1")
數列2[2] = 1
print("數列1:", 數列1)
print("數列2:", 數列2)
print("說明: 改變數列2的值 數列1毀跟著變化")
print("因為數列只有一個基礎 不會被複製")
print('')

