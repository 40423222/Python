### list( ) and tuple( )
### 改變列表跟元祖的類型
print("改變列表跟元祖的類型")
print("list( ) and tuple( ):")
print("定義列表: 數列 = [1, 2, 3]")
數列 = [1, 2, 3]
print("定義元組: 數組 = (1, 2, 3)")
數組 = (1, 2, 3)
print('')

print("現在為列表")
print("type(數列):", type(數列))
print("數列:", 數列)
print('')

print("轉成元組")
print("使用: tuple(數列)")
print("type(tuple(數列)):", type(tuple(數列)))
print("tuple(數列):", tuple(數列))
print('')

print("現在為元組")
print("type(數組):", type(數組))
print("數組:", 數組)
print('')

print("轉成列表")
print("使用: list(數組)")
print("type(list(數組)):", type(list(數組)))
print("list(數組):", list(數組))
print('')

