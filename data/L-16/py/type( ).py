### 顯示值的類型
print("顯示值的類型")
print("type( ):")
print('列表')
print("type(['listValue',])", type(['listValue',]))
print('')

print('元組')
print("type(('hello',)):", type(('hello',)))
print('')

print('字串')
print("type(('hello')):", type(('hello')))
print('')

print('整數')
print("type(123):", type(123))
print('')

print('浮點數')
print("type(0.123):", type(0.123))
print('')

